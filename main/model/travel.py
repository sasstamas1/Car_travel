class Travel:
    def __init__(self, carsnumber, from_, where, date, km, fuel):
        self.carsnumber = carsnumber
        self.from_ = from_
        self.where = where
        self.date = date
        self.km = km
        self.fuel = fuel


def getCarsnumber(self):
    return self.carsnumber


def getFrom(self):
    return self.from_


def getWhere(self):
    return self.where


def getDate(self):
    return self.date


def getKm(self):
    return self.km


def getFuel(self):
    return self.fuel
